export interface Reference {
  quote: string;
  page_number: number;
}

export interface ChatResponse {
  chinese_answer: string;
  english_answer: string;
  references: Reference[];
}

export async function askQuestion(question: string, apiKey: string): Promise<ChatResponse> {
  const ai = new GoogleGenAI({ apiKey });
  
  const systemInstruction = `
你是一位文學研究專家，專門協助學生理解提供的這本小說。
所有回答必須嚴格基於以下提供的文本。如果文本中沒有相關信息，請禮貌地告知學生。

資料來源文本：
${FULL_NOVEL_TEXT}

請按照以下 JSON 格式回答：
{
  "chinese_answer": "繁體中文的詳細回答",
  "english_answer": "A concise English summary of the answer",
  "references": [
    {
      "quote": "原文引用 1",
      "page_number": 12 // 引用所在的估計頁數
    },
    {
      "quote": "原文引用 2",
      "page_number": 15
    }
  ]
}

如果只有一個引用，陣列中也請包含該對象。請盡可能列出所有相關的引用。
請確保回答富有啟發性且準確。
`;

  const response = await ai.models.generateContent({
    model: "gemini-3-flash-preview",
    contents: question,
    config: {
      systemInstruction,
      responseMimeType: "application/json",
      responseSchema: {
        type: Type.OBJECT,
        properties: {
          chinese_answer: { type: Type.STRING },
          english_answer: { type: Type.STRING },
          references: {
            type: Type.ARRAY,
            items: {
              type: Type.OBJECT,
              properties: {
                quote: { type: Type.STRING },
                page_number: { type: Type.NUMBER },
              },
              required: ["quote", "page_number"],
            },
          },
        },
        required: ["chinese_answer", "english_answer", "references"],
      },
    },
  });

  const text = response.text || "{}";
  return JSON.parse(text) as ChatResponse;
}
